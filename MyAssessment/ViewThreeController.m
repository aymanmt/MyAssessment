#import "ViewThreeController.h"

@interface ViewThreeController ()

@property (strong, nonatomic) NSArray *dates;

@end

@implementation ViewThreeController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    
    // populate dates array with up to 12 days starting from today
    NSDateFormatter *formatter =  [[NSDateFormatter alloc] init];
    [formatter setDateFormat:@"dd MMMM yyyy"];
    
    NSCalendar *calendar = [NSCalendar currentCalendar];
    
    NSMutableArray *temp = [[NSMutableArray alloc] init];
    
    for (int i = 0; i < 12; i++) {
        [temp addObject:[formatter stringFromDate:[calendar dateByAddingUnit:NSCalendarUnitDay
                                                                       value:i
                                                                      toDate:[NSDate date]
                                                                     options:kNilOptions]]];
    }
    
    self.dates = temp.mutableCopy;
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {

    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {

    // Return the number of rows in the section.
    return [self.dates count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"DateCell" forIndexPath:indexPath];
    
    // Configure the cell...
    if (cell != nil) {
        cell.textLabel.text = _dates[indexPath.row];
    }
    
    return cell;
}


